type LangCtx = {
  lang: Lang;
  /** Languages the shop owner has switched on, in display order. */
  enabled: Lang[];
  setLang: (l: Lang) => void;
  t: (key: string) => string;
  pick: (en: string, el?: string) => string;
};

const Ctx = createContext<LangCtx | null>(null);

const COOKIE_RE = /(?:^|; )atelier_lang=(en|el|de|fr|it|es|nl|pl|pt|sv|sq)/;

function readCookie(): Lang | null {
  if (typeof document === "undefined") return null;
  const m = document.cookie.match(COOKIE_RE);
  return (m?.[1] as Lang) ?? null;
}

function writeCookie(l: Lang) {
  document.cookie = `atelier_lang=${l}; path=/; max-age=${60 * 60 * 24 * 365}`;
}

/** Keep only supported codes and guarantee English is present, mirroring the
 *  server's loadEnabledLanguages(). Falls back to English only. */
function normalizeEnabled(list: unknown): Lang[] {
  const valid = Array.isArray(list)
    ? list.filter(
        (l): l is Lang =>
          typeof l === "string" && SUPPORTED_LANGS.includes(l as Lang),
      )
    : [];
  if (valid.length === 0) return ["en"];
  return valid.includes("en") ? valid : ["en", ...valid];
}

export function LangProvider({
  children,
  enabled,
}: {
  children: ReactNode;
  /** From settings.json `enabledLanguages`. Defaults to English only. */
  enabled?: Lang[];
}) {
  // Pages are ISR / statically cached, so the `enabled` prop the server
  // layout passes can lag behind settings.json. Seed state from it for the
  // first paint, then refresh from /api/languages on mount so the admin
  // Site Languages panel takes effect without a rebuild.
  const [enabledLangs, setEnabledLangs] = useState<Lang[]>(() =>
    normalizeEnabled(enabled),
  );
  const enabledKey = enabledLangs.join(",");
  const [lang, setLangState] = useState<Lang>("en");

  useEffect(() => {
    let cancelled = false;
    fetch(withBasePath("/api/languages"))
      .then((r) => (r.ok ? r.json() : null))
      .then((data: { enabled?: unknown } | null) => {
        if (cancelled || !data) return;
        const next = normalizeEnabled(data.enabled);
        setEnabledLangs((prev) =>
          prev.join(",") === next.join(",") ? prev : next,
        );
      })
      .catch(() => {
        /* offline or 404: keep the set the server layout provided */
      });
    return () => {
      cancelled = true;
    };
  }, []);

  useEffect(() => {
    const allowed = enabledKey.split(",") as Lang[];
    const fromCookie = readCookie();
    if (fromCookie && allowed.includes(fromCookie)) {
      setLangState(fromCookie);
      return;
    }
    const browser = (navigator.language || "").toLowerCase();
    const matched = SUPPORTED_LANGS.find((l) => browser.startsWith(l));
    if (matched && allowed.includes(matched)) {
      setLangState(matched);
    } else {
      setLangState("en");
    }
  }, [enabledKey]);

  // Reflect the current language on <html lang> so screen readers, search
  // engines and browser spell-checkers pick the right locale.
  useEffect(() => {
    if (typeof document !== "undefined") {
      document.documentElement.lang = lang;
    }
  }, [lang]);

  const value = useMemo<LangCtx>(
    () => ({
      lang,
      enabled: enabledKey.split(",") as Lang[],
      setLang: (l) => {
        const allowed = enabledKey.split(",") as Lang[];
        const next = allowed.includes(l) ? l : "en";
        setLangState(next);
        writeCookie(next);
      },
      t: (key) => {
        const v = STRINGS[lang]?.[key] ?? STRINGS.en[key] ?? key;
        return Array.isArray(v) ? v.join("") : v;
      },
      pick: (en, el) => (lang === "el" && el ? el : en),
    }),
    [lang, enabledKey]
  );

  return <Ctx.Provider value={value}>{children}</Ctx.Provider>;
}

export function useLang(): LangCtx {
  const c = useContext(Ctx);
  if (!c) {
    return {
      lang: "en",
      enabled: ["en"],
      setLang: () => {},
      t: (k) => (STRINGS.en[k] as string) ?? k,
      pick: (en) => en,
    };
  }
  return c;
}
